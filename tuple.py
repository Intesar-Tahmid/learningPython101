# A tuple is a kind of data structure in python
# Tuple is pretty much like list, but better than that

coordinates = [(4, 5, 7), (6, 7), (8, 9)]
# A tuple is fixed. You cannot change or modify a tuple.
# So if you try to assign values in specific indices python will show error
print(coordinates)
print(coordinates[0])
print(coordinates[1])

# So to store data that we're not going to change at all, we'll use tuples
